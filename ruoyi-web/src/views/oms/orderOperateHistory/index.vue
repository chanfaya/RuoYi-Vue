<template>
  <div class="p-2">
    <transition :enter-active-class="proxy?.animate.searchAnimate.enter" :leave-active-class="proxy?.animate.searchAnimate.leave">
      <div v-show="showSearch" class="mb-[10px]">
        <el-card shadow="hover">
          <el-form ref="queryFormRef" :model="queryParams" :inline="true">
            <el-form-item label="订单id" prop="orderId">
              <el-input v-model="queryParams.orderId" placeholder="请输入订单id" clearable @keyup.enter="handleQuery" />
            </el-form-item>
            <el-form-item label="操作人[用户；系统；后台管理员]" prop="operateMan">
              <el-input v-model="queryParams.operateMan" placeholder="请输入操作人[用户；系统；后台管理员]" clearable @keyup.enter="handleQuery" />
            </el-form-item>
            <el-form-item label="备注" prop="note">
              <el-input v-model="queryParams.note" placeholder="请输入备注" clearable @keyup.enter="handleQuery" />
            </el-form-item>
            <el-form-item>
              <el-button type="primary" icon="Search" @click="handleQuery">搜索</el-button>
              <el-button icon="Refresh" @click="resetQuery">重置</el-button>
            </el-form-item>
          </el-form>
        </el-card>
      </div>
    </transition>

    <el-card shadow="never">
      <template #header>
        <el-row :gutter="10" class="mb8">
          <el-col :span="1.5">
            <el-button type="primary" plain icon="Plus" @click="handleAdd" v-hasPermi="['oms:orderOperateHistory:add']">新增</el-button>
          </el-col>
          <el-col :span="1.5">
            <el-button type="success" plain icon="Edit" :disabled="single" @click="handleUpdate()" v-hasPermi="['oms:orderOperateHistory:edit']">修改</el-button>
          </el-col>
          <el-col :span="1.5">
            <el-button type="danger" plain icon="Delete" :disabled="multiple" @click="handleDelete()" v-hasPermi="['oms:orderOperateHistory:remove']">删除</el-button>
          </el-col>
          <el-col :span="1.5">
            <el-button type="warning" plain icon="Download" @click="handleExport" v-hasPermi="['oms:orderOperateHistory:export']">导出</el-button>
          </el-col>
          <right-toolbar v-model:showSearch="showSearch" @queryTable="getList"></right-toolbar>
        </el-row>
      </template>

      <el-table v-loading="loading" :data="orderOperateHistoryList" @selection-change="handleSelectionChange">
        <el-table-column type="selection" width="55" align="center" />
        <el-table-column label="" align="center" prop="id" v-if="true" />
        <el-table-column label="订单id" align="center" prop="orderId" />
        <el-table-column label="操作人[用户；系统；后台管理员]" align="center" prop="operateMan" />
        <el-table-column label="订单状态【0->待付款；1->待发货；2->已发货；3->已完成；4->已关闭；5->无效订单】" align="center" prop="orderStatus" />
        <el-table-column label="备注" align="center" prop="note" />
        <el-table-column label="操作" align="center" class-name="small-padding fixed-width">
          <template #default="scope">
            <el-tooltip content="修改" placement="top">
              <el-button link type="primary" icon="Edit" @click="handleUpdate(scope.row)" v-hasPermi="['oms:orderOperateHistory:edit']"></el-button>
            </el-tooltip>
            <el-tooltip content="删除" placement="top">
              <el-button link type="primary" icon="Delete" @click="handleDelete(scope.row)" v-hasPermi="['oms:orderOperateHistory:remove']"></el-button>
            </el-tooltip>
          </template>
        </el-table-column>
      </el-table>

      <pagination v-show="total > 0" :total="total" v-model:page="queryParams.pageNum" v-model:limit="queryParams.pageSize" @pagination="getList" />
    </el-card>
    <!-- 添加或修改订单操作历史记录对话框 -->
    <el-dialog :title="dialog.title" v-model="dialog.visible" width="500px" append-to-body>
      <el-form ref="orderOperateHistoryFormRef" :model="form" :rules="rules" label-width="80px">
        <el-form-item label="订单id" prop="orderId">
          <el-input v-model="form.orderId" placeholder="请输入订单id" />
        </el-form-item>
        <el-form-item label="操作人[用户；系统；后台管理员]" prop="operateMan">
          <el-input v-model="form.operateMan" placeholder="请输入操作人[用户；系统；后台管理员]" />
        </el-form-item>
        <el-form-item label="备注" prop="note">
            <el-input v-model="form.note" type="textarea" placeholder="请输入内容" />
        </el-form-item>
      </el-form>
      <template #footer>
        <div class="dialog-footer">
          <el-button :loading="buttonLoading" type="primary" @click="submitForm">确 定</el-button>
          <el-button @click="cancel">取 消</el-button>
        </div>
      </template>
    </el-dialog>
  </div>
</template>

<script setup name="OrderOperateHistory" lang="ts">
import { listOrderOperateHistory, getOrderOperateHistory, delOrderOperateHistory, addOrderOperateHistory, updateOrderOperateHistory } from '@/api/oms/orderOperateHistory';
import { OrderOperateHistoryVO, OrderOperateHistoryQuery, OrderOperateHistoryForm } from '@/api/oms/orderOperateHistory/types';

const { proxy } = getCurrentInstance() as ComponentInternalInstance;

const orderOperateHistoryList = ref<OrderOperateHistoryVO[]>([]);
const buttonLoading = ref(false);
const loading = ref(true);
const showSearch = ref(true);
const ids = ref<Array<string | number>>([]);
const single = ref(true);
const multiple = ref(true);
const total = ref(0);

const queryFormRef = ref<ElFormInstance>();
const orderOperateHistoryFormRef = ref<ElFormInstance>();

const dialog = reactive<DialogOption>({
  visible: false,
  title: ''
});

const initFormData: OrderOperateHistoryForm = {
  id: undefined,
  orderId: undefined,
  operateMan: undefined,
  orderStatus: undefined,
  note: undefined
}
const data = reactive<PageData<OrderOperateHistoryForm, OrderOperateHistoryQuery>>({
  form: {...initFormData},
  queryParams: {
    pageNum: 1,
    pageSize: 10,
    orderId: undefined,
    operateMan: undefined,
    orderStatus: undefined,
    note: undefined,
    params: {
    }
  },
  rules: {
    id: [
      { required: true, message: "不能为空", trigger: "blur" }
    ],
    orderId: [
      { required: true, message: "订单id不能为空", trigger: "blur" }
    ],
    operateMan: [
      { required: true, message: "操作人[用户；系统；后台管理员]不能为空", trigger: "blur" }
    ],
    orderStatus: [
      { required: true, message: "订单状态【0->待付款；1->待发货；2->已发货；3->已完成；4->已关闭；5->无效订单】不能为空", trigger: "change" }
    ],
    note: [
      { required: true, message: "备注不能为空", trigger: "blur" }
    ]
  }
});

const { queryParams, form, rules } = toRefs(data);

/** 查询订单操作历史记录列表 */
const getList = async () => {
  loading.value = true;
  const res = await listOrderOperateHistory(queryParams.value);
  orderOperateHistoryList.value = res.rows;
  total.value = res.total;
  loading.value = false;
}

/** 取消按钮 */
const cancel = () => {
  reset();
  dialog.visible = false;
}

/** 表单重置 */
const reset = () => {
  form.value = {...initFormData};
  orderOperateHistoryFormRef.value?.resetFields();
}

/** 搜索按钮操作 */
const handleQuery = () => {
  queryParams.value.pageNum = 1;
  getList();
}

/** 重置按钮操作 */
const resetQuery = () => {
  queryFormRef.value?.resetFields();
  handleQuery();
}

/** 多选框选中数据 */
const handleSelectionChange = (selection: OrderOperateHistoryVO[]) => {
  ids.value = selection.map(item => item.id);
  single.value = selection.length != 1;
  multiple.value = !selection.length;
}

/** 新增按钮操作 */
const handleAdd = () => {
  reset();
  dialog.visible = true;
  dialog.title = "添加订单操作历史记录";
}

/** 修改按钮操作 */
const handleUpdate = async (row?: OrderOperateHistoryVO) => {
  reset();
  const _id = row?.id || ids.value[0]
  const res = await getOrderOperateHistory(_id);
  Object.assign(form.value, res.data);
  dialog.visible = true;
  dialog.title = "修改订单操作历史记录";
}

/** 提交按钮 */
const submitForm = () => {
  orderOperateHistoryFormRef.value?.validate(async (valid: boolean) => {
    if (valid) {
      buttonLoading.value = true;
      if (form.value.id) {
        await updateOrderOperateHistory(form.value).finally(() =>  buttonLoading.value = false);
      } else {
        await addOrderOperateHistory(form.value).finally(() =>  buttonLoading.value = false);
      }
      proxy?.$modal.msgSuccess("操作成功");
      dialog.visible = false;
      await getList();
    }
  });
}

/** 删除按钮操作 */
const handleDelete = async (row?: OrderOperateHistoryVO) => {
  const _ids = row?.id || ids.value;
  await proxy?.$modal.confirm('是否确认删除订单操作历史记录编号为"' + _ids + '"的数据项？').finally(() => loading.value = false);
  await delOrderOperateHistory(_ids);
  proxy?.$modal.msgSuccess("删除成功");
  await getList();
}

/** 导出按钮操作 */
const handleExport = () => {
  proxy?.download('oms/orderOperateHistory/export', {
    ...queryParams.value
  }, `orderOperateHistory_${new Date().getTime()}.xlsx`)
}

onMounted(() => {
  getList();
});
</script>
